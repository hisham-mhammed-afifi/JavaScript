8 * null // 0
"5" - 1 // 4
"5" + 1 // 51
"five" + 2 // NaN
false == 0 // true
null == undefined // true
null == 0 // false

// **Short-Circuiting
null || "user" // user
"Hesham" || "user" // Hesham